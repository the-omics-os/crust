package main

import "github.com/the-omics-os/crust/moleculeviewer"

type sample struct {
	name     string
	smiles   string
	molBlock string
}

var samples = []sample{
	{
		name:     "Caffeine",
		smiles:   "CN1C=NC2=C1C(=O)N(C(=O)N2C)C",
		molBlock: caffeineMolBlock,
	},
	{
		name:     "Aspirin",
		smiles:   "CC(=O)OC1=CC=CC=C1C(=O)O",
		molBlock: aspirinMolBlock,
	},
	{
		name:     "Nicotine",
		smiles:   "CN1CCC[C@H]1C2=CN=CC=C2",
		molBlock: nicotineMolBlock,
	},
}

func loadSample(index int) moleculeviewer.Molecule {
	s := samples[index]
	if s.molBlock == "" {
		mol, err := moleculeviewer.ParseSMILES(s.smiles)
		if err != nil {
			panic(err)
		}
		mol.Name = s.name
		return mol
	}

	mol, err := moleculeviewer.ParseMolBlock(s.molBlock)
	if err != nil {
		panic(err)
	}
	mol = mol.WithoutHydrogens()
	mol.Name = s.name
	mol.SMILES = s.smiles
	return mol
}

